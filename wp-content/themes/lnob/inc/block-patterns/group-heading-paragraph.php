<h2 class="alignwide">Den sociala tryggheten är otillräcklig och ojämnt fördelad</h2>
<!-- /wp:heading -->

<!-- wp:group -->
<div class="wp-block-group"><!-- wp:paragraph -->
<p>Sociala trygghetssystem, som till exempel sjukpenning, pensioner och barnbidrag, är avgörande för att minska fattigdomen. Pandemin har visat hur viktiga de är när människors hälsa och försörjning hotas.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>2020 täcktes bara halva världens befolkning av någon typ av grundläggande skydd, vilket innebär att 4 miljarder människor står helt utan skyddsnät.</p>
<!-- /wp:paragraph -->

